using Sngty;
using System.Collections;
using System.Collections.Generic;
using TMPro;
using UnityEngine;
using UnityEngine.SceneManagement;

public class ExampleCommunicator : MonoBehaviour
{
    [Header("Singularity Setup")]
    public SingularityManager mySingularityManager;
    public string DeviceName;
    private DeviceSignature myDevice = new DeviceSignature();

    [Header("UI References")]
    public GameObject UIInterfacePanel;
    public TMP_Text _debugText;
    public GameObject environment;
    public GameObject objectToHide;

    [Header("Spaceship Status")]
    public SpaceshipStatusController spaceshipStatusController;  // Drag the controller here

    [HideInInspector]
    public int touch;

    void Start()
    {
        List<DeviceSignature> pairedDevices = mySingularityManager.GetPairedDevices();

        // Look for a specific device by name
        foreach (var device in pairedDevices)
        {
            if (DeviceName.Equals(device.name))
            {
                myDevice = device;
                _debugText.text = "Found " + device.mac.ToString();
                mySingularityManager.ConnectToDevice(myDevice);
                break;
            }
            else
            {
                _debugText.text = "No match";
            }
        }
    }

    public void onConnected()
    {
        Debug.Log("Connected to device");
        _debugText.text = "Connected to the Microcontroller";
        mySingularityManager.sendMessage("1", myDevice);  // Test message
    }

    public void onMessageRecieved(string message)
    {
        Debug.Log("Message received: " + message);

        UIInterfacePanel.SetActive(false);

        // Handle object visibility messages
        if (message == "on") objectToHide.SetActive(true);
        else if (message == "off") objectToHide.SetActive(false);

        // Parse touch values
        if (int.TryParse(message, out touch))
        {
            // Combine touch value and spaceship status into a single string
            string status = (touch >= 1000) ? "Activated!" : "Deactivated!";
            string syncUp = (touch <= 900 && touch >= 300) ? "Activated!" : "Deactivated!";
            if (status.Equals("Activated!") && SceneManager.GetActiveScene().name == "TitleScreen"){
                SceneManager.LoadScene("cobaltsphereandkepler");
            }
            if (syncUp.Equals("Activated!") && SceneManager.GetActiveScene().name == "cobaltpart2")
            {
                SceneManager.LoadScene("cobaltpart3");
            }

            // Update debug text
            _debugText.text = $"Message received: {message}\nTouch value: {touch}\nEngine status: {status}\nSync status: {syncUp}";

            // Optional debug log
            Debug.Log($"Touch={touch}, Spaceship status={status}");
        }
    }

    public void onError(string errorMessage)
    {
        Debug.LogError("Error with Singularity: " + errorMessage);
        _debugText.text = "Error with Singularity: " + errorMessage;
    }

    // Optional commands to send messages to Arduino
    public void sendGrabMessage() => mySingularityManager.sendMessage("grab", myDevice);
    public void sendReleaseMessage() => mySingularityManager.sendMessage("release", myDevice);
}




















































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































